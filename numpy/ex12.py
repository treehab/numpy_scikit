'''
sorting arrays
    sort()
'''

import numpy as np
arr = np.array([3,2,0,1])
print(np.sort(arr))

arr = np.array(['banana', 'cherry', 'apple', 'fig'])
print(np.sort(arr))

arr = np.array([True, False, False, True])
print(np.sort(arr))


#2D array
arr = np.array([[3, 2, 4], [5, 0, 1]])
print(np.sort(arr))

